Console.WriteLine("Hello, World!");

var FirstNumber = 10;

if(FirstNumber == 10)
{
    Console.WriteLine("Equal Numbers");
}

if(FirstNumber!= 20)
{
    Console.WriteLine("Numbers not equal");
}

if(FirstNumber < 40)
{
    Console.WriteLine("Number is Less Than");
}

if (FirstNumber > 4)
{
    Console.WriteLine("Number is Less Than");
}

if(FirstNumber <= 40)
{
    Console.WriteLine("Number less than or equal to");
}
if (FirstNumber >= 4)
{
    Console.WriteLine("Number greater than or equal to");
}

